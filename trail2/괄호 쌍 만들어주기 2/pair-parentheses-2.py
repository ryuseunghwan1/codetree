# 문자열 입력 받기
A = input()

cnt = 0
n = len(A)

# 1. 연속한 여는 괄호 '(('의 시작 위치 i를 찾습니다.
# i+1을 확인해야 하므로 범위는 n - 1까지입니다.
for i in range(n - 1):
    if A[i] == '(' and A[i + 1] == '(':
        
        # 2. i 뒤쪽에서 연속한 닫는 괄호 '))'의 시작 위치 j를 찾습니다.
        # j+1을 확인해야 하므로 범위는 n - 1까지이며, 최소 i + 2부터 시작합니다.
        for j in range(i + 2, n - 1):
            if A[j] == ')' and A[j + 1] == ')':
                cnt += 1  # 올바른 쌍을 찾았으므로 카운트 증가

# 결과 출력
print(cnt)